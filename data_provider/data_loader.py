class Dataset_Custom_Hour(Dataset):
    def __init__(self, args, root_path, flag='train', size=None,
                 features='MS', data_path='tgcn/clean_data.csv',
                 target='Adjusted_Demand_MWh', scale=True,
                 timeenc=0, freq='h', seasonal_patterns=None):
        # size = [seq_len, label_len, pred_len]
        self.args = args

        if size is None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        assert flag in ['train', 'val', 'test']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path

        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()

        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))

        # construct date column
        df_raw['date'] = pd.to_datetime(
            df_raw[['Year', 'Month', 'Day', 'Hour']].rename(
                columns={
                    'Year': 'year',
                    'Month': 'month',
                    'Day': 'day',
                    'Hour': 'hour'
                }
            )
        )

        # resort by date
        df_raw = df_raw.sort_values('date').reset_index(drop=True)

        # split the train and test dataset
        n = len(df_raw)
        num_train = int(n * 0.7)
        num_test = int(n * 0.2)
        num_val = n - num_train - num_test

        border1s = [
            0,
            num_train - self.seq_len,
            num_train + num_val - self.seq_len
        ]
        border2s = [
            num_train,
            num_train + num_val,
            n
        ]

        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features in ['M', 'MS']:
            cols_drop = ['date', 'Year', 'Month', 'Day', 'Hour', 'Date1', 'Weekday','Holiday', 'Adjusted_Generation_MWh']
            cols_data = [c for c in df_raw.columns if c not in cols_drop]
            df_data = df_raw[cols_data]

        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        # standardized
        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        # date encoder
        df_stamp = df_raw[['date']][border1:border2].copy()
        df_stamp['date'] = pd.to_datetime(df_stamp['date'])

        if self.timeenc == 0:
            df_stamp['month'] = df_stamp['date'].apply(lambda row: row.month)
            df_stamp['day'] = df_stamp['date'].apply(lambda row: row.day)
            df_stamp['weekday'] = df_stamp['date'].apply(lambda row: row.weekday())
            df_stamp['hour'] = df_stamp['date'].apply(lambda row: row.hour)

            data_stamp = df_stamp.drop(columns=['date']).values

        elif self.timeenc == 1:
            data_stamp = time_features(
                pd.to_datetime(df_stamp['date'].values),
                freq=self.freq
            )
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

        if self.set_type == 0 and hasattr(self.args, "augmentation_ratio") and self.args.augmentation_ratio > 0:
            self.data_x, self.data_y, augmentation_tags = run_augmentation_single(
                self.data_x, self.data_y, self.args
            )

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len

        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

class Dataset_WECC(Dataset):
    """
    Dataset for a single WECC Balancing Authority with spatial neighbor support.

    Each sample returns the target BA's (x, y, x_mark, y_mark) plus the
    input windows of all first-order neighbors (same time slice) and the
    subgraph adjacency matrix for the GNN.

    Args:
        ba_name         : e.g. 'CISO'
        neighbor_indices: list of int  — global BA indices of direct neighbors
        ba_names        : ordered list of 28 BA names (index reference)
        data_root       : folder containing *_cleaned_historical_data.csv files
        full_adj_np     : (28, 28) binary adjacency matrix (numpy)
        ba_idx          : global index of this BA in ba_names
        All other args mirror Dataset_Custom_Hour.
    """

    _COLS_DROP = {
        'Year', 'Month', 'Day', 'Hour', 'Time_UTC',
        'Cleaning_Code', 'Demand_Change_MWh', 'Forecast_Demand_MWh',
    }

    def __init__(
        self,
        args,
        ba_name: str,
        neighbor_indices: list,
        ba_names: list,
        data_root: str,
        full_adj_np=None,
        ba_idx: int = None,
        flag: str = 'train',
        size=None,
        features: str = 'MS',
        target: str = 'Cleaned_Demand_MWh',
        scale: bool = True,
        timeenc: int = 0,
        freq: str = 'h',
        root_path=None,
        data_path=None,
        seasonal_patterns=None,
        year=None,             # if set, filter data to this calendar year only
        pi_root_1=None,          # path to PI folder (e.g. 'PI'); if None, topology disabled
        pi_root_2=None,
        # kept for backward compat
        sim_weights=None,
    ):
        self.args = args
        self.ba_name = ba_name
        self.neighbor_indices = neighbor_indices
        self.ba_names = ba_names
        self.data_root = data_root
        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq
        self.full_adj_np = full_adj_np
        self.ba_idx = ba_idx
        self.year = year
        self.pi_root_1 = pi_root_1
        self.pi_root_2 = pi_root_2
        self.flag = flag

        if size is None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len, self.label_len, self.pred_len = size

        assert flag in ['train', 'val', 'test']
        self.set_type = {'train': 0, 'val': 1, 'test': 2}[flag]

        self._read_data()

    def _load_ba_csv(self, ba: str):
        path = os.path.join(self.data_root, f"{ba}_cleaned_historical_data.csv")
        df = pd.read_csv(path)
        df['date'] = pd.to_datetime(
            df[['Year', 'Month', 'Day', 'Hour']].rename(
                columns={'Year': 'year', 'Month': 'month',
                         'Day': 'day',   'Hour': 'hour'}
            )
        )
        return df.sort_values('date').reset_index(drop=True)

    def _select_features(self, df):
        if self.features in ['M', 'MS']:
            cols = [c for c in df.columns
                    if c not in self._COLS_DROP and c != 'date']
        else:
            cols = [self.target]
        vals = df[cols].copy()
        vals = vals.fillna(vals.mean())
        return vals.values.astype(np.float32)

    def _split_borders(self, n: int):
        num_train = int(n * 0.7)
        num_test  = int(n * 0.2)
        num_val   = n - num_train - num_test
        border1s = [0,
                    num_train - self.seq_len,
                    num_train + num_val - self.seq_len]
        border2s = [num_train,
                    num_train + num_val,
                    n]
        return border1s, border2s

    def _read_data(self):
        df = self._load_ba_csv(self.ba_name)
        if self.year is not None:
            df = df[df['date'].dt.year == self.year].reset_index(drop=True)
        n = len(df)
        border1s, border2s = self._split_borders(n)
        b1, b2 = border1s[self.set_type], border2s[self.set_type]

        raw_vals = self._select_features(df)

        self.scaler = StandardScaler()
        if self.scale:
            self.scaler.fit(raw_vals[border1s[0]:border2s[0]])
            data = self.scaler.transform(raw_vals)
        else:
            data = raw_vals

        self.data_x = data[b1:b2]
        self.data_y = data[b1:b2]

        df_stamp = df[['date']].iloc[b1:b2].copy()
        if self.timeenc == 0:
            self.data_stamp = np.stack([
                df_stamp['date'].dt.month.values,
                df_stamp['date'].dt.day.values,
                df_stamp['date'].dt.weekday.values,
                df_stamp['date'].dt.hour.values,
            ], axis=1)
        else:
            self.data_stamp = time_features(
                pd.to_datetime(df_stamp['date'].values), freq=self.freq
            ).T

        if (self.set_type == 0
                and hasattr(self.args, 'augmentation_ratio')
                and self.args.augmentation_ratio > 0):
            self.data_x, self.data_y, _ = run_augmentation_single(
                self.data_x, self.data_y, self.args
            )

        # Load neighbor BAs (same time split)
        self.neighbor_data = []
        for idx in self.neighbor_indices:
            nb_ba = self.ba_names[idx]
            df_nb = self._load_ba_csv(nb_ba)
            if self.year is not None:
                df_nb = df_nb[df_nb['date'].dt.year == self.year].reset_index(drop=True)
            raw_nb = self._select_features(df_nb)

            scaler_nb = StandardScaler()
            if self.scale:
                scaler_nb.fit(raw_nb[border1s[0]:border2s[0]])
                data_nb = scaler_nb.transform(raw_nb)
            else:
                data_nb = raw_nb

            self.neighbor_data.append(data_nb[b1:b2])

        # Build subgraph adjacency matrix (N_total × N_total)
        # Node order: target first (index 0), then neighbors in sorted order
        if self.full_adj_np is not None and self.ba_idx is not None:
            all_nodes = sorted([self.ba_idx] + self.neighbor_indices)
            self.subgraph_adj = torch.tensor(
                self.full_adj_np[np.ix_(all_nodes, all_nodes)],
                dtype=torch.float32
            )
        else:
            # Fallback: target connected to all neighbors, no neighbor-neighbor edges
            N_total = len(self.neighbor_indices) + 1
            adj = np.zeros((N_total, N_total), dtype=np.float32)
            adj[0, 1:] = 1.0
            adj[1:, 0] = 1.0
            self.subgraph_adj = torch.tensor(adj)

        # Load PI images if pi_root and year are provided
        self.pi_images_1 = None
        self.pi_images_2 = None
        
        if self.pi_root_1 is not None and self.year is not None:
            pi_path_1 = os.path.join(
                self.pi_root_1,
                f"{self.ba_name}_cleaned_historical_data",
                f"year_{self.year}",
                f"{self.flag}_pi_images.npy"
            )
            if os.path.exists(pi_path_1):
                self.pi_images_1 = np.load(pi_path_1).astype(np.float32)  # (N, 10, 10)

        if self.pi_root_2 is not None and self.year is not None:
            pi_path_2 = os.path.join(
                self.pi_root_2,
                f"{self.ba_name}",
                f"year_{self.year}",
                f"{self.flag}_pi_images.npy"
            )
            if os.path.exists(pi_path_2):
                self.pi_images_2 = np.load(pi_path_2).astype(np.float32)  # (N, 10, 10)


    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def __getitem__(self, index):
        s_begin = index
        s_end   = s_begin + self.seq_len
        r_begin = s_end   - self.label_len
        r_end   = r_begin + self.label_len + self.pred_len

        seq_x      = self.data_x[s_begin:s_end]
        seq_y      = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        if self.neighbor_data:
            neighbor_xs = np.stack(
                [nb[s_begin:s_end] for nb in self.neighbor_data], axis=0
            )                                                # (N, L, n_vars)
        else:
            neighbor_xs = np.zeros(
                (0, self.seq_len, seq_x.shape[-1]), dtype=np.float32
            )                                                # isolated node

        # 6th element: subgraph adjacency (N_total, N_total) — same for every sample
        # 7th element: PI image (1, 10, 10) or zeros if not available
        if self.pi_images_1 is not None and index < len(self.pi_images_1):
            pi_img_1 = self.pi_images_1[index][np.newaxis, :, :]   # (1, 10, 10)
        else:
            pi_img_1 = np.zeros((1, 10, 10), dtype=np.float32)

        if self.pi_images_2 is not None and index < len(self.pi_images_2):
            pi_img_2 = self.pi_images_2[index][np.newaxis, :, :]   # (1, 10, 10)
        else:
            pi_img_2 = np.zeros((1, 10, 10), dtype=np.float32)

        return seq_x, seq_y, seq_x_mark, seq_y_mark, neighbor_xs, self.subgraph_adj, pi_img_1, pi_img_2

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)
